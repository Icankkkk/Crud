    public void TampilkanData() {
        DftTblModel_mhs = new DefaultTableModel();
        DftTblModel_mhs.addColumn("Nim");
        DftTblModel_mhs.addColumn("Nama");
        DftTblModel_mhs.addColumn("Kelas");
        DftTblModel_mhs.addColumn("Jurusan");
        jTable_mhs.setModel(DftTblModel_mhs);
        Connection conn = Koneksi.getConnection();
        // try connecting to MySql
        try {
          java.sql.Statement stmt = conn.createStatement();
          SQL = "select * from maba";
          java.sql.ResultSet res = stmt.executeQuery(SQL);
          // looping
          while (res.next()) {
              DftTblModel_mhs.addRow(new Object[] {
                res.getString("nim_mhs"),   
                res.getString("nama_mhs"),
                res.getString("kelas_mhs"),
                res.getString("jurusan_mhs")
            });
          }
          
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    private void cariData(String key) {
        try {
           Object[] judul_kolom = {"Nim Mahasiswa", "Nama Mahasiswa", "Kelas", "Jurusan"};
           DftTblModel_mhs = new DefaultTableModel(null, judul_kolom);
           jTable_mhs.setModel(DftTblModel_mhs);
           
           Connection conn = Koneksi.getConnection();
           Statement stmt = conn.createStatement();
           DftTblModel_mhs.getDataVector().removeAllElements();
           
           RsMaba=stmt.executeQuery("SELECT * from maba WHERE nim_mhs LIKE '%"+key+"%' OR nama_mhs LIKE '%"+key+"%' OR jurusan_mhs LIKE '%"+key+"%' OR kelas_mhs LIKE '%"+key+"%' ");
           while (RsMaba.next()) {
                Object[] data = {
                RsMaba.getString("nim_mhs"),   
                RsMaba.getString("nama_mhs"),
                RsMaba.getString("kelas_mhs"),
                RsMaba.getString("jurusan_mhs")
            };
            DftTblModel_mhs.addRow(data);
           }
        } catch (SQLException e) {
            System.err.println(e.getMessage());
        }
    }
    
