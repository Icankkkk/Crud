    // method untuk menghapus data 
    private void jButton_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_hapusActionPerformed
        // TODO add your handling code here:
        Connection conn = Koneksi.getConnection();
            int confirm = JOptionPane.showConfirmDialog(null, "Anjirr seriusan lu mau di apus ?", "Konfirmasi", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == 0) { 
                try {   
                    java.sql.PreparedStatement stmt = conn.prepareStatement("DELETE from maba WHERE nim_mhs = '" + jTextField_nim_mhs.getText() + "'");
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data dah berhasil dihapus euyy :P", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    TampilkanData();
                    jTextField_nim_mhs.setText("");
                    jTextField_nama_mhs.setText("");
                    jTextField_kelas_mhs.setText("");
                    jTextField_jurusan_mhs.setText("");
                    jTextField_nim_mhs.requestFocus();   
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Data gagal di hapus :(" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
                }
            }
    }//GEN-LAST:event_jButton_hapusActionPerformed
    
